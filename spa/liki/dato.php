class Principal
{

  function suma($dato1,$dato2,$dato3,$dato4)
  {
    $suma= ($dato1 + $dato2 + $dato3 + $dato4);

    return $suma;
  }

  function multiplica($dato1,$dato2,$dato3)
  {
    $multiplica= ($dato1 * $dato2 * $dato3);

    return $multiplica;
  }

  function cuadrado($dato1,$dato2)
  {
    $cuadrado= ($dato1*$dato1) + ($dato2*$dato2);

    return $cuadrado;
  }
}


